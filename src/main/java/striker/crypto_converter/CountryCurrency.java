package striker.crypto_converter;

/**
 * This class is an object representation of a country's currency code.
 */
public class CountryCurrency {
    private String code;
    private String name;

    /**
     * Accessor method for {@code code}.
     * @return String the value of {@code code}.
     */
    public String getCode() {
        return this.code;
    }

    /**
     * Accessor method for {@code name}.
     * @return String the value of {@code name}.
     */
    public String getName() {
        return this.name;
    }
}
